class Lugares {
    constructor(id, pais, provincia, localidad, img, imgLateral, categoria) {
        this.id = parseInt(id);
        this.pais = pais.toUpperCase();
        this.provincia = provincia.toUpperCase();
        this.localidad = localidad.toUpperCase();
        this.img = img;
        this.imgLateral = imgLateral;
        this.categoria = categoria;
    }
    //metodos
};
